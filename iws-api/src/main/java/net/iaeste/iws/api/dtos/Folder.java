package net.iaeste.iws.api.dtos;

import net.iaeste.iws.api.constants.IWSConstants;
import net.iaeste.iws.api.util.AbstractVerification;
import net.iaeste.iws.api.util.Date;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * @author  Kim Jensen / last $Author:$
 * @version $Revision:$ / $Date:$
 * @since   IWS 1.1
 */
public final class Folder extends AbstractVerification {

    /** {@link IWSConstants#SERIAL_VERSION_UID}. */
    private static final long serialVersionUID = IWSConstants.SERIAL_VERSION_UID;

    private String folderId = null;
    private String parentId = null;
    private Group group = null;
    private String foldername = null;
    private List<Folder> folders = null;
    private List<File> files = null;
    private Date modified = null;
    private Date created = null;

    // =========================================================================
    // Object Constructors
    // =========================================================================

    /**
     * Empty Constructor, to use if the setters are invoked. This is required
     * for WebServices to work properly.
     */
    public Folder() {
    }

    /**
     * Copy Constructor.
     *
     * @param file File Object to copy
     */
    public Folder(final Folder folder) {
        if (folder != null) {
            this.folderId = folder.folderId;
            this.parentId = folder.parentId;
            this.group = new Group(folder.group);
            this.foldername = folder.foldername;
            this.folders = folder.folders;
            this.files = folder.files;
            this.created = folder.created;
        }
    }

    // =========================================================================
    // Standard Setters & Getters
    // =========================================================================

    /**
     * Sets the Folder Id for this Folder Object. The Id is generated by the IWS
     * upon saving the Folder.<br />
     *   Note, that if the value is invalid, then the method will thrown an
     * {@code IllegalArgumentException}.
     *
     * @param folderId Folder Id
     * @throws IllegalArgumentException if the Folder Id is invalid
     */
    public void setFolderId(final String folderId) throws IllegalArgumentException {
        ensureValidId("folderId", folderId);
        this.folderId = folderId;
    }

    public String getFolderId() {
        return folderId;
    }

    /**
     * Sets the Parent Folder Id for this Folder. If no ParentId is given, upon
     * creation of the Folder, the Group's root folder will be used.
     *
     * @param parentId Parent Folder Id
     */
    public void setParentId(final String parentId) throws IllegalArgumentException {
        ensureValidId("parentId", parentId);
        this.parentId = parentId;
    }

    public String getParentId() {
        return parentId;
    }

    /**
     * The owning group for this Folder. All Folder management is handled via
     * the Group, and this this is a mandatory field.<br />
     *   The method will throw an {@code IllegalArgumentException} if the Group
     * is either null or invalid.
     *
     * @param group Group
     * @throws IllegalArgumentException if null or not verifiable
     */
    public void setGroup(final Group group) throws IllegalArgumentException {
        ensureNotNullAndVerifiable("group", group);
        this.group = group;
    }

    public Group getGroup() {
        return group;
    }

    /**
     * The Foldername is mandatory in the IWS, and if not set or too long, the
     * method will throw an {@code IllegalArgumentException}.
     *
     * @param filename Name of the Folder
     * @throws IllegalArgumentException if null, empty or longer than 100 characters
     */
    public void setFoldername(final String foldername) throws IllegalArgumentException {
        ensureNotNullOrEmptyOrTooLong("foldername", foldername, 100);
        this.foldername = foldername;
    }

    public String getFoldername() {
        return foldername;
    }

    /**
     * List of sub Folders for the current Folder. The list is set by the IWS,
     * and any changes to it will be ignored.
     *
     * @param folders List of sub Folders, empty if none.
     */
    public void setFolders(final List<Folder> folders) {
        this.folders = folders;
    }

    public List<Folder> getFolders() {
        return folders == null ? new ArrayList<Folder>(0) : folders;
    }

    /**
     * List of Files for the current Folder. The lis tis set by the IWS, and
     * any changes to it will be ignored.
     *
     * @param files List of Files, empty if none.
     */
    public void setFiles(final List<File> files) {
        this.files = files;
    }

    public List<File> getFiles() {
        return files == null ? new ArrayList<File>(0) : files;
    }

    /**
     * Sets the last time the file was modified. Note, that this value is
     * controlled by the IWS, so any changes to it will be ignored.
     *
     * @param modified Date of last modification
     */
    public void setModified(final Date modified) {
        this.modified = modified;
    }

    public Date getModified() {
        return modified;
    }

    /**
     * Sets the time the file was created. Note, that this value is controlled
     * by the IWS, so any changes to it will be ignored.
     *
     * @param created Date of creation
     */
    public void setCreated(final Date created) {
        this.created = created;
    }

    public Date getCreated() {
        return created;
    }

    // =========================================================================
    // DTO required methods
    // =========================================================================

    /**
     * {@inheritDoc}
     */
    @Override
    public Map<String, String> validate() {
        final Map<String, String> validation = new HashMap<>(0);

        isNotNull(validation, "foldername", foldername);
        isNotNull(validation, "group", group);

        return validation;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (!(o instanceof Folder)) {
            return false;
        }

        final Folder folder = (Folder) o;

        if (folderId != null ? !folderId.equals(folder.folderId) : folder.folderId != null) {
            return false;
        }
        if (parentId != null ? !parentId.equals(folder.parentId) : folder.parentId != null) {
            return false;
        }
        if (group != null ? !group.equals(folder.group) : folder.group != null) {
            return false;
        }
        if (foldername != null ? !foldername.equals(folder.foldername) : folder.foldername != null) {
            return false;
        }
        if (folders != null ? !folders.equals(folder.folders) : folder.folders != null) {
            return false;
        }
        if (files != null ? !files.equals(folder.files) : folder.files != null) {
            return false;
        }
        if (modified != null ? !modified.equals(folder.modified) : folder.modified != null) {
            return false;
        }
        if (created != null ? !created.equals(folder.created) : folder.created != null) {
            return false;
        }

        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        int result = IWSConstants.HASHCODE_INITIAL_VALUE;

        result = IWSConstants.HASHCODE_MULTIPLIER * result + ((folderId != null) ? folderId.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + ((parentId != null) ? parentId.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + ((group != null) ? group.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + (foldername != null ? foldername.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + (folders != null ? folders.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + (files != null ? files.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + (modified != null ? modified.hashCode() : 0);
        result = IWSConstants.HASHCODE_MULTIPLIER * result + (created != null ? created.hashCode() : 0);

        return result;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        return "Folder{" +
                "folderId='" + folderId + '\'' +
                ", parentId='" + parentId + '\'' +
                ", group='" + group + '\'' +
                ", foldername='" + foldername + '\'' +
                ", folders=" + folders +
                ", files=" + files +
                ", modified=" + modified +
                ", created=" + created +
                '}';
    }
}
