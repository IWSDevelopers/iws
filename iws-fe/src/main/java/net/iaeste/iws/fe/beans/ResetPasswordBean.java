package net.iaeste.iws.fe.beans;

import org.hibernate.validator.constraints.Length;

import javax.enterprise.context.RequestScoped;
import javax.inject.Named;

/**
 * Setting the password's length between 6 and 20 and a confirmation password if a user wishes to reset his password
 *
 * @author Marko Cilimkovic / last $Author:$
 * @version $Revision:$ / $Date:$
 * @since 1.7
 */
@Named
@RequestScoped
public class ResetPasswordBean {

    @Length(min = 6, max= 20)
    private String password;
    private String confirmPassword;

    // TODO add action method to reset password once the API methods are ready

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getConfirmPassword() {
        return confirmPassword;
    }

    public void setConfirmPassword(String confirmPassword) {
        this.confirmPassword = confirmPassword;
    }
}
